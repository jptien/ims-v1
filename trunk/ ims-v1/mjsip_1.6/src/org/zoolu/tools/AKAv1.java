	public int[] ComputeOPc(int[] op_t, int [] op_c) {
		int i;
		op_c = this.RijndaelEncrypt(op_t);

		for (i = 0; i < 16; i++)
			op_c[i] ^= op_t[i];
		
		return op_c;
	} /* end of function ComputeOPc */
	
	public int[] RijndaelEncrypt(int[] input) {
		int[] output = new int[16];
		int[][] state = new int[4][4];
		int i, r;

		/* initialise state array from input char string */
		for (i = 0; i < 16; i++)
			state[i & 0x3][i >> 2] = input[i];

		/* add first round_key */
		KeyAdd(state, roundKeys, 0);

		/* do lots of full rounds */
		for (r = 1; r <= 9; r++) {
			byteSub(state);
			ShiftRow(state);
			MixColumn(state);
			KeyAdd(state, roundKeys, r);
		}

		/* final round */
		byteSub(state);
		ShiftRow(state);
		KeyAdd(state, roundKeys, r);

		/* produce output char string from state array */
		for (i = 0; i < 16; i++) {
			output[i] = state[i & 0x3][i >> 2];
		}

		return output;
	} /* end of function RijndaelEncrypt */


	public void RijndaelKeySchedule(int[] key) {
		int roundConst;
		int i, j;

		/* first round key equals key */
		for (i = 0; i < 16; i++)
			roundKeys[0][i & 0x03][i >> 2] = key[i];

		roundConst = 1;

		/* now calculate round keys */
		for (i = 1; i < 11; i++) {
			roundKeys[i][0][0] = (S[roundKeys[i - 1][1][3]]
					^ roundKeys[i - 1][0][0] ^ roundConst);
			roundKeys[i][1][0] = (S[roundKeys[i - 1][2][3]] ^ roundKeys[i - 1][1][0]);
			roundKeys[i][2][0] = (S[roundKeys[i - 1][3][3]] ^ roundKeys[i - 1][2][0]);
			roundKeys[i][3][0] = (S[roundKeys[i - 1][0][3]] ^ roundKeys[i - 1][3][0]);

			for (j = 0; j < 4; j++) {
				roundKeys[i][j][1] = (roundKeys[i - 1][j][1] ^ roundKeys[i][j][0]);
				roundKeys[i][j][2] = (roundKeys[i - 1][j][2] ^ roundKeys[i][j][1]);
				roundKeys[i][j][3] = (roundKeys[i - 1][j][3] ^ roundKeys[i][j][2]);
			}

			/* update round constant */
			roundConst = Xtime[roundConst];
		}

		return;
	} /* end of function RijndaelKeySchedule */

	/* Round key addition function */
	public void KeyAdd(int[][] state, int[][][] roundKeys, int round) {
		int i, j;

		for (i = 0; i < 4; i++)
			for (j = 0; j < 4; j++)
				state[i][j] ^= roundKeys[round][i][j];

		return ;
	}

	public void byteSub(int[][] state) {
		int i, j;

		for (i = 0; i < 4; i++)
			for (j = 0; j < 4; j++)
				state[i][j] = S[state[i][j]];

		return ;
	}

	/* Row shift transformation */
	public void ShiftRow(int[][] state) {
		int temp;

		/* left rotate row 1 by 1 */
		temp = state[1][0];
		state[1][0] = state[1][1];
		state[1][1] = state[1][2];
		state[1][2] = state[1][3];
		state[1][3] = temp;

		/* left rotate row 2 by 2 */
		temp = state[2][0];
		state[2][0] = state[2][2];
		state[2][2] = temp;
		temp = state[2][1];
		state[2][1] = state[2][3];
		state[2][3] = temp;

		/* left rotate row 3 by 3 */
		temp = state[3][0];
		state[3][0] = state[3][3];
		state[3][3] = state[3][2];
		state[3][2] = state[3][1];
		state[3][1] = temp;

		return;
	}

