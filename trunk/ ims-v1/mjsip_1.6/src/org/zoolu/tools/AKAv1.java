	public void MixColumn(int[][] state) {
		int temp, tmp, tmp0;
		int i;

		/* do one column at a time */
		for (i = 0; i < 4; i++) {
			temp = (char) (state[0][i] ^ state[1][i] ^ state[2][i] ^ state[3][i]);
			tmp0 = state[0][i];

			/* Xtime array does multiply by x in GF2^8 */
			tmp = (char) Xtime[state[0][i] ^ state[1][i]];
			state[0][i] ^= temp ^ tmp;

			tmp = (char) Xtime[state[1][i] ^ state[2][i]];
			state[1][i] ^= temp ^ tmp;

			tmp = (char) Xtime[state[2][i] ^ state[3][i]];
			state[2][i] ^= temp ^ tmp;

			tmp = (char) Xtime[state[3][i] ^ tmp0];
			state[3][i] ^= temp ^ tmp;
		}

		return;
	}


