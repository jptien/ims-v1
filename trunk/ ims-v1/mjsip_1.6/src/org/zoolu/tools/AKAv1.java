		int[] temp = new int[16];
		int[] in1 = new int[16];
		int[] out1 = new int[16];
		int[] rijndaelInput = new int[16];
		int i;

		int[] rand = AV.getRAND();
		RijndaelKeySchedule(k);

		op_c = ComputeOPc(op_t, op_c);

		for (i = 0; i < 16; i++)
			rijndaelInput[i] = (int) (rand[i] ^ op_c[i]);
		temp = RijndaelEncrypt(rijndaelInput);

		for (i = 0; i < 6; i++) {
			in1[i] = sqn[i];
			in1[i + 8] = sqn[i];
		}
